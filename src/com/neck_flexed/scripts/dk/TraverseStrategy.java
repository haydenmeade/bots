package com.neck_flexed.scripts.dk;

public enum TraverseStrategy {
    FremennikBoots4("Fremennik Sea Boots 4"),
    House("House (Portal or location)"),
    FairyRing("Fairy Ring");

    private final String name;

    TraverseStrategy(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }
}

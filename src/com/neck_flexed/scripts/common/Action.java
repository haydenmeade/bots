package com.neck_flexed.scripts.common;

public enum Action {
    Attack(),
    Alch(),
    Consume(),
    Loot(),
    Move(),
    RedClick(),
    Spec(),
    Equip(),
    None();
    private static Action action;

    private Action() {
    }

    public static void set(Action a) {
        action = a;
    }

    public static Action get() {
        return action;
    }
}
